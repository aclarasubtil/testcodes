public class ExerciciosBasicos_07 {

        public static void main(String[] args) {
                int soma = 0;  
        long produto = 1;  
        for (int i = 1; i < 30; i+= 2) {  
            soma += i;  
            produto *= (i+1);  
            }  
        
        System.out.println("A soma dos números ímpares é: "+soma);  
        System.out.println("A multiplicação dos números pares é: "+produto);
        
        }  
}
                 
   
  
