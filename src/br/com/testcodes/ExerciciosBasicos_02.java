package br.com.testcodes;

public class ExerciciosBasicos_02 {
    public static void main (String []args) {
        // Exercício 02
        // 2.1
        int i; // Aqui declaramos a variável inteira i.
        i = 1; // Aqui inicializamos, ou seja, atribuímos um valor, a variável i que foi declarada acima.
        System.out.println(i); // Aqui imprimimos o valor de i, note a ausência das aspas duplas quando queremos imprimir o valor de uma variável.
        
        // 2.2 //Esses seguem o mesmo conceito, só mudam o tipo das variáveis.
        long j; // O tipo long serve para nuúmeros inteiros muito grandes...
        j = 2;
        System.out.println(j);
        
        // 2.3
        float p;
        p = 20;
        System.out.println(p);
        
        // 2.4
        double q;
        q = 30.0;
        System.out.println(q);
        
        // 2.5
        boolean b; // Um boolean, só pode ser true ou false, ou seja, verdadeiro ou falso.
        b = true; // Nesse caso, dissemos que ele é verdadeiro.
        System.out.println(b);
        
        // 2.6
        char c; // O tipo char é usado para guardar apenas um caractere, pode ser uma letra, número ou símbolo.
        c = 'k'; // Aqui definimos ele como sendo k.
        System.out.println(c);
    }
}
        
       